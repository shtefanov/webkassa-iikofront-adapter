function getWebkassaErrorInfo(code) {
  const normalized = normalizeWebkassaCode(code);
  return WEBKASSA_ERROR_CATALOG[normalized] || WEBKASSA_ERROR_CATALOG['-1'];
}

function normalizeWebkassaCode(code) {
  if (code === undefined || code === null || code === '') return '-1';
  return String(code).trim();
}

